/** Años típicos para filtrar (desde 1970 hasta año actual) — parque venezolano con vehículos de 30+ años */
export function obtenerAnios(): number[] {
  const actual = new Date().getFullYear();
  const anios: number[] = [];
  for (let y = actual; y >= 1970; y--) anios.push(y);
  return anios;
}

export const ANOS = obtenerAnios();
